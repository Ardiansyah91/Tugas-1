/* Effek dari operator ++ */
public class Incr {
    public static void main(String[] args) {
        // Kamus
        int i, j;
        // Program
        i = 3;
        j = i++;
        System.out.println("Nilai i : " + (++i) + "\nNilai j : " + j);
    }
}
